    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        String servidor;
        if (jCheckBoxServidor.isSelected() == true) {
            servidor = "S";
        } else {
            servidor = "N";
        }

        verificarPropriedades.salvaPropriedades(servidor, jLabelCaminhoBanco.getText() + jTextFieldCaminhoBancoDados.getText() +"/"+ jTextFieldNomeBanco.getText(),
            jTextFieldNomeBanco.getText(), jTextFieldUser.getText(),
            jPasswordFieldPass.getText(), "S");
        try {
            conexao.conexao();
            JOptionPane.showMessageDialog(null, "Para finalizar esse procedimento, o sistema precisa ser fechado.");
            conexao.conexao().close();
            System.exit(0);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro em escrever no arquivo: " + ex);
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

