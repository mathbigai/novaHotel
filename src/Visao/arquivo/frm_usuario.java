    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        verificaCamposVazios();
        if (flagCamposVazios == 1) {
            modalUsuario.setNome(jTextFieldNomeUsuario.getText());
            modalUsuario.setLogin(jTextFieldLogin.getText());
            modalUsuario.setTipo(String.valueOf(jComboBoxTipo.getSelectedItem()));
            modalUsuario.setInativo(false);
            if (flag == 1) {
                try {
                    daoUsuario.create(modalUsuario);
                    esquemaBotoesInicio();
                    limpaCampos();
                    atualizaTabela("select * from tbl_usuario");
                    conn.rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_usuario.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                modalUsuario.setId(Integer.parseInt(jTextFieldCodigo.getText()));
                modalUsuario.setInativo(jCheckBoxInativo.isSelected());
                try {
                    daoUsuario.update(modalUsuario);
                    esquemaBotoesInicio();
                    limpaCampos();
                    atualizaTabela("select * from tbl_usuario");
                    conn.rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_usuario.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

