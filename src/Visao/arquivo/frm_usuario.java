    public void atualizaTabela(String sql) {
        tableModelUsuario.dados.removeAll(tableModelUsuario.dados);
        conn.executaSQL(sql);
        try {
            while (conn.rs.next()) {
                ModalUsuario modalQ = new ModalUsuario();

                modalQ.setId(conn.rs.getInt("usuario_id"));
                modalQ.setNome(conn.rs.getString("usuario_nome"));
                modalQ.setLogin(conn.rs.getString("usuario_login"));
                modalQ.setInativo(conn.rs.getBoolean("usuario_inativo"));
                tableModelUsuario.addRow(modalQ);
            }
            conn.rs.close();
        } catch (SQLException ex) {
            Logger.getLogger(frm_usuario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    private void bloquiaCampos() {
        if (jCheckBoxInativo.isSelected() == true) {
            jTextFieldNomeUsuario.setEnabled(false);
            jTextFieldLogin.setEnabled(false);
            jComboBoxTipo.setEnabled(false);
            jButtonAlterarSenha.setEnabled(false);
        } else {
            jTextFieldNomeUsuario.setEnabled(true);
            jTextFieldLogin.setEnabled(true);
            jComboBoxTipo.setEnabled(true);
            jButtonAlterarSenha.setEnabled(true);
        }

    }

