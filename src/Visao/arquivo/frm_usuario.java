    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        flag = 2;
        if (jTableConsulta.getSelectedRow() != -1) {
            jTextFieldCodigo.setText(jTableConsulta.getValueAt(jTableConsulta.getSelectedRow(), 0).toString());
            conn.executaSQL("SELECT * FROM tbl_usuario WHERE usuario_id = " + jTextFieldCodigo.getText());
            try {
                if (conn.rs.next()) {
                    jTextFieldNomeUsuario.setText(conn.rs.getString("usuario_nome"));
                    jTextFieldLogin.setText(conn.rs.getString("usuario_login"));
                    jComboBoxTipo.setSelectedItem(conn.rs.getString("usuario_tipo"));
                    jCheckBoxInativo.setSelected(conn.rs.getBoolean("usuario_inativo"));
                    senha = conn.rs.getString("usuario_senha");
                }
                conn.rs.close();
            } catch (SQLException ex) {

            }
            esquemaBotoesSalvar();
        } else {
            JOptionPane.showMessageDialog(null, "Para alterar, selecione algum item.");
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

