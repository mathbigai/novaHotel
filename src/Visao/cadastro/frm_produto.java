    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        verificaCamposVazios();
        if (flagCamposVazios == 1) {
            modalProduto.setNome(jTextFieldNomeProduto.getText());
            modalProduto.setValor(Double.parseDouble(jTextFieldValorProduto.getText()));
            modalProduto.setQuantidade(Integer.parseInt(jTextFieldQuantidadeProduto.getText()));
            if (flag == 1) {
                try {
                    daoProduto.create(modalProduto);
                    esquemaBotoesInicio();
                    limpaCampos();
                    atualizaTabela("select * from tbl_produto");
                    conn.rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_produto.class.getName()).log(Level.SEVERE, null, ex);
                }

            } else {
                modalProduto.setId(Integer.parseInt(jTextFieldCodigo.getText()));
                try {
                    daoProduto.update(modalProduto);
                    esquemaBotoesInicio();
                    limpaCampos();
                    atualizaTabela("select * from tbl_produto");
                    conn.rs.close();
                } catch (SQLException ex) {
                    Logger.getLogger(frm_produto.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButtonSalvarActionPerformed

    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        flag = 2;
        if (jTableConsulta.getSelectedRow() != -1) {
            jTextFieldCodigo.setText(jTableConsulta.getValueAt(jTableConsulta.getSelectedRow(), 0).toString());
            jTextFieldNomeProduto.setText(jTableConsulta.getValueAt(jTableConsulta.getSelectedRow(), 1).toString());
            jTextFieldQuantidadeProduto.setText(jTableConsulta.getValueAt(jTableConsulta.getSelectedRow(), 3).toString());
            conn.executaSQL("SELECT produto_valor FROM tbl_produto WHERE produto_id = " + jTextFieldCodigo.getText());
            try {
                if (conn.rs.next()) {
                    jTextFieldValorProduto.setText(String.valueOf(conn.rs.getDouble("produto_valor")));
                }
                conn.rs.close();
            } catch (SQLException ex) {

            }
            esquemaBotoesSalvar();
        } else {
            JOptionPane.showMessageDialog(null, "Para alterar, selecione algum item.");
        }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

