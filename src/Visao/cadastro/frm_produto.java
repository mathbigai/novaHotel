    private void verificaCamposVazios() {
        if (jTextFieldNomeProduto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo: Nome, estÃ¡ em branco");
            jTextFieldNomeProduto.requestFocus();
            jTextFieldNomeProduto.setBackground(color);
        } else {
            jTextFieldNomeProduto.setBackground(colorPadrao);
        }
        if (jTextFieldQuantidadeProduto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo: Quantidade, estÃ¡ em branco");
            jTextFieldQuantidadeProduto.requestFocus();
            jTextFieldQuantidadeProduto.setBackground(color);
        } else {
            jTextFieldQuantidadeProduto.setBackground(colorPadrao);
        }
        if (jTextFieldValorProduto.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Campo: Valor do Produto, estÃ¡ em branco");
            jTextFieldValorProduto.requestFocus();
            jTextFieldValorProduto.setBackground(color);
        } else {
            jTextFieldValorProduto.setBackground(colorPadrao);
        }
        if (jTextFieldNomeProduto.getBackground() == colorPadrao
                && jTextFieldQuantidadeProduto.getBackground() == colorPadrao
                && jTextFieldValorProduto.getBackground() == colorPadrao) {
            flagCamposVazios = 1;
        } else {
            flagCamposVazios = 0;
        }
    }

